#pragma once
#include "Engine/GameObject.h"
#include"Player.h"
#include"Enemy.h"

//画面右下のミニマップを表示するクラス
class MiniMap :
    public GameObject
{
private:
	//----------インスタンス----------
	Player* pPlayer_;
	Enemy* pEnemy_;

	//----------マップ上の位置----------
	XMFLOAT3 playerPos_;
	XMFLOAT3 enemyPos_;

public:
	MiniMap(GameObject* parent);
	~MiniMap();

	void Initialize() override;
	void Update() override;
	void Draw() override;
	void Release() override;

	void SetCSV();

	XMFLOAT3 GetPlayerPos() { return playerPos_; }
	XMFLOAT3 GetEnemyPos() { return enemyPos_; }
};

